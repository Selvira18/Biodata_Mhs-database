    private void doConnect() throws SQLException {
        try {
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            String host = "jdbc:mysql://localhost:3306/latihan_db";
            String username = "root";
            String password = "";
            koneksi = DriverManager.getConnection(host, username, password);
            
             stetement = (Statement) koneksi.createStatement();
            String sql = "SELECT*FROM biodata_mhs";
             result= stetement.executeQuery(sql);
             
            //pindahkan kursor pada record pertama
            result.first();
            tampilData();
            
        } catch (ClassNotFoundException | SQLException | InstantiationException 
                | IllegalAccessException ex) {
            Logger.getLogger(BiodataMhs.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void tampilData() throws SQLException {
        txtNim.setText(result.getString("Nim"));
        txtNama.setText(result.getString("Nama"));
        
        String kelamin = result.getString("Jenis Kelamin");
        switch(kelamin){
            case"L":
                rbLaki.setSelected(true);
                break;
            case"P":
                rbPerempuan.setSelected(true);
                break;
        }
        txtTanggal.setText(result.getString("Tanggal Lahir"));
        txtAlamat.setText(result.getString("Alamat"));
        txtFak.setText(result.getString("Fakultas"));
        txtJur.setText(result.getString("Jurusan"));
        txtAngkatan.setText(result.getString("Angkatan"));
    }

