    private void bnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnTambahActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            final String username = "root";
            final String password = "";
            final Connection koneksi = DriverManager.getConnection(
            "jdbc:mysql://localhots:3306/latihan_db",username,password);
            
            PreparedStatement prepare = koneksi
                    .prepareStatement("INSERT INTO biodata_mhs"
                    +"(nim,nama,jenis_kelamin,tanggal_lahir,alamat,"
                    +"fakultas,jurusan,angkatan)"
                    +"VALUES(?,?,?,?,?,?,?,?,)"
                    );
                    prepare.setString(1,txtNim.getText());
                    prepare.setString(2,txtNama.getText());
                    String kelamin=null;
                    if(rbLaki.isSelected())
                        kelamin = "L";
                    else if(rbPerempuan.isSelected())
                        kelamin ="P";
                    prepare.setString(3,kelamin);
                    
                    SimpleDateFormat formater = new SimpleDateFormat("dd/MM/yyyy");
                    try{
                        java.util.Date date = formater.parse(txtTanggal.getText());
                        java.sql.Date sqlDate = new java.sql.Date(date.getTime());
                        prepare.setDate(4, (java.sql.Date) sqlDate);
                    } catch (ParseException e) {
                    }
                    prepare.setString(5, txtAlamat.getText());
                    prepare.setString(6, txtFak.getText());
                    prepare.setString(7, txtJur.getText());
                    prepare.setString(8, txtAngkatan.getText());
                    prepare.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan.");
        } catch (final SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TambahData.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InstantiationException ex) {
            Logger.getLogger(TambahData.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalAccessException ex) {
            Logger.getLogger(TambahData.class.getName()).log(Level.SEVERE, null, ex);
                    } 
    }//GEN-LAST:event_bnTambahActionPerformed

    private void bnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnSimpanActionPerformed
         try {
            
            result.updateString("Nim",txtNim.getText());
            result.updateString("Nama", txtNama.getText());
            
            String kelamin=null;
            if(rbLaki.isSelected())
                kelamin = "L";
            else if(rbPerempuan.isSelected())
                kelamin = "P";
            result.updateString("Jenis Kelamin", kelamin);
            
            SimpleDateFormat formater = new SimpleDateFormat("dd/MM/yyyy");
            
            try {
                java.util.Date date = formater.parse(txtTanggal.getText());
                java.sql.Date sqlDate = new java.sql.Date(date.getTime());
                result.updateDate("Tanggal Lahir",(java.sql.Date)sqlDate);
            } catch (ParseException ex) {
                }
            
            result.updateString("Alamat", txtAlamat.getText());
            result.updateString("Fakultas", txtFak.getText());
            result.updateString("Jurusan", txtJur.getText());
            result.updateString("Angkatan",txtAngkatan.getText());
            
            result.updateRow();
            
            JOptionPane.showMessageDialog(null,"Data berhasil diubah.");
            
        } catch (final SQLException ex) {
             try {
                 JOptionPane.showMessageDialog(null,ex.getMessage());
                 stetement = koneksi.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,
                         ResultSet.CONCUR_UPDATABLE);
                 String sql ="SELECT * FROM biodata_mhs";
                 result = stetement.executeQuery(sql);
             } catch (SQLException ex1) {
                 Logger.getLogger(BiodataMhs.class.getName()).log(Level.SEVERE, null, ex1);
             }
        }
         
    }//GEN-LAST:event_bnSimpanActionPerformed

